package com.TP.Locy.form;

public class AnnonceForm {
	
	private String id;
	private String titre;
	private String description;
	private String nb_chambres; 
	private String nb_voyageurs;
	private String prix;
	private String photo;
	private String adresse;
	private String localisation;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * @return the titre
	 */
	public String getTitre() {
		return titre;
	}
	/**
	 * @param titre the titre to set
	 */
	public void setTitre(String titre) {
		this.titre = titre;
	}
	
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
	/**
	 * @return the nb_chambres
	 */
	public String getNb_chambres() {
		return nb_chambres;
	}
	/**
	 * @param nb_chambres the nb_chambres to set
	 */
	public void setNb_chambres(String nb_chambres) {
		this.nb_chambres = nb_chambres;
	}
	
	/**
	 * @return the nb_voyageurs
	 */
	public String getNb_voyageurs() {
		return nb_voyageurs;
	}
	/**
	 * @param nb_voyageurs the nb_voyageurs to set
	 */
	public void setNb_voyageurs(String nb_voyageurs) {
		this.nb_voyageurs = nb_voyageurs;
	}
	
	/**
	 * @return the prix
	 */
	public String getPrix() {
		return prix;
	}
	/**
	 * @param prix the prix to set
	 */
	public void setPrix(String prix) {
		this.prix = prix;
	}
	
	/**
	 * @return the photo
	 */
	public String getPhoto() {
		return photo;
	}
	/**
	 * @param photo the photo to set
	 */
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
	/**
	 * @return the adresse
	 */
	public String getAdresse() {
		return adresse;
	}
	/**
	 * @param adresse the adresse to set
	 */
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	
	/**
	 * @return the localisation
	 */
	public String getLocalisation() {
		return localisation;
	}
	/**
	 * @param localisation the localisation to set
	 */
	public void setLocalisation(String localisation) {
		this.localisation = localisation;
	}
	

}
